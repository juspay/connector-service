// Manual connector structure to avoid macro conflicts
use std::marker::PhantomData;

#[derive(Clone)]
pub struct Billdesk<T> {
    amount_converter: &'static dyn AmountConvertor<Output = StringMinorUnit> + Sync,
    connector_name: &'static str,
    payment_method_data: PhantomData<T>,
}

impl<T: std::fmt::Debug> std::fmt::Debug for Billdesk<T> {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        f.debug_struct("Billdesk")
            .field("connector_name", &self.connector_name)
            .field("payment_method_data", &self.payment_method_data)
            .finish()
    }
}

impl<T> Billdesk<T> {
    pub fn new() -> &'static Self {
        &Self {
            amount_converter: &common_utils::types::StringMinorUnitForConnector,
            connector_name: "billdesk",
            payment_method_data: PhantomData,
        }
    }
}

// Manual implementation for Authorize flow to avoid macro conflicts
impl<
        T: PaymentMethodDataTypes
            + std::fmt::Debug
            + std::marker::Sync
            + std::marker::Send
            + 'static
            + Serialize,
    > ConnectorIntegrationV2<Authorize, PaymentFlowData, PaymentsAuthorizeData<T>, PaymentsResponseData>
    for Billdesk<T>
{
    fn build_request_v2(
        &self,
        req: &RouterDataV2<Authorize, PaymentFlowData, PaymentsAuthorizeData<T>, PaymentsResponseData>,
    ) -> CustomResult<Option<common_utils::request::Request>, errors::ConnectorError> {
        let request = transformers::BilldeskPaymentsRequest::try_from((req.clone(), self.clone()))?;
        let url = self.get_url(req)?;
        let headers = self.get_headers(req)?;
        
        Ok(Some(common_utils::request::Request {
            url,
            method: common_utils::request::Method::Post,
            headers: headers.into_iter().collect(),
            body: Some(common_utils::request::RequestContent::Json(Box::new(request))),
            certificate: None,
            certificate_key: None,
            ca_certificate: None,
        }))
    }
}

impl<
        T: PaymentMethodDataTypes
            + std::fmt::Debug
            + std::marker::Sync
            + std::marker::Send
            + 'static
            + Serialize,
    > Billdesk<T> {
    fn get_headers(
        &self,
        req: &RouterDataV2<Authorize, PaymentFlowData, PaymentsAuthorizeData<T>, PaymentsResponseData>,
    ) -> CustomResult<Vec<(String, Maskable<String>)>, errors::ConnectorError> {
        let mut header = vec![(
            headers::CONTENT_TYPE.to_string(),
            self.common_get_content_type().to_string().into(),
        )];

        let auth_type = transformers::BilldeskAuth::try_from(&req.connector_auth_type)?;

        let mut auth_header = get_billdesk_auth_header(&auth_type)?;

        header.append(&mut auth_header);
        Ok(header)
    }
    
    fn get_url(
        &self,
        req: &RouterDataV2<Authorize, PaymentFlowData, PaymentsAuthorizeData<T>, PaymentsResponseData>,
    ) -> CustomResult<String, errors::ConnectorError> {
        let is_test = req.resource_common_data.test_mode.unwrap_or(false);
        let base_url = if is_test {
            "https://uat.billdesk.com/pgidsk/PGIDirectRequest?reqid=BDRDF011"
        } else {
            "https://www.billdesk.com/pgidsk/PGIDirectRequest?reqid=BDRDF011"
        };
        Ok(base_url.to_string())
    }
}

impl<
        T: PaymentMethodDataTypes
            + std::fmt::Debug
            + std::marker::Sync
            + std::marker::Send
            + 'static
            + Serialize,
    > ConnectorCommon for Billdesk<T>
{
    fn id(&self) -> &'static str {
        "billdesk"
    }

    fn get_currency_unit(&self) -> CurrencyUnit {
        CurrencyUnit::Base
    }

    fn common_get_content_type(&self) -> &'static str {
        "application/json"
    }

    fn base_url<'a>(&self, connectors: &'a Connectors) -> &'a str {
        connectors.billdesk.base_url.as_ref()
    }

    fn get_auth_header(
        &self,
        _auth_type: &ConnectorAuthType,
    ) -> CustomResult<Vec<(String, Maskable<String>)>, errors::ConnectorError> {
        // Billdesk uses custom auth in get_headers
        Ok(vec![])
    }

    fn build_error_response(
        &self,
        res: Response,
        event_builder: Option<&mut ConnectorEvent>,
    ) -> CustomResult<ErrorResponse, errors::ConnectorError> {
        let response: billdesk::BilldeskErrorResponse = res
            .response
            .parse_struct("BilldeskErrorResponse")
            .change_context(errors::ConnectorError::ResponseDeserializationFailed)?;

        with_error_response_body!(event_builder, response);

        Ok(ErrorResponse {
            status_code: res.status_code,
            code: response.error.to_string(),
            message: response.error_description.clone().unwrap_or_default(),
            reason: response.error_description.clone(),
            attempt_status: None,
            connector_transaction_id: None,
            network_advice_code: None,
            network_decline_code: None,
            network_error_message: None,
        })
    }
}

