#[derive(Clone)]
pub struct Getnet<T: PaymentMethodDataTypes> {
    amount_converter: &'static (dyn common_utils::types::AmountConvertor<Output = common_utils::types::FloatMajorUnit> + Sync),
    payment_method_type: std::marker::PhantomData<T>,
}

impl<T: PaymentMethodDataTypes> std::fmt::Debug for Getnet<T> {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        f.debug_struct("Getnet")
            .field("payment_method_type", &self.payment_method_type)
            .finish()
    }
}

impl<T: PaymentMethodDataTypes> Getnet<T> {
    pub const fn new() -> &'static Self {
        &Self {
            amount_converter: &common_utils::types::FloatMajorUnitForConnector,
            payment_method_type: std::marker::PhantomData,
        }
    }
}

