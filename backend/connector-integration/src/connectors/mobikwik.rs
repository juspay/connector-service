macros::macro_connector_implementation!(
    connector_default_implementations: [get_content_type, get_error_response_v2],
    connector: Mobikwik,
    curl_request: Json(MobikwikPaymentsRequest),
    curl_response: MobikwikPaymentsResponse,
    flow_name: Authorize,
    resource_common_data: PaymentFlowData,
    flow_request: PaymentsAuthorizeData<T>,
    flow_response: PaymentsResponseData,
    http_method: Post,
    generic_type: T,
    [PaymentMethodDataTypes + std::fmt::Debug + std::marker::Sync + std::marker::Send + 'static + Serialize],
    other_functions: {
        fn get_headers(
            &self,
            req: &RouterDataV2<Authorize, PaymentFlowData, PaymentsAuthorizeData<T>, PaymentsResponseData>,
        ) -> CustomResult<Vec<(String, Maskable<String>)>, errors::ConnectorError> {
            let mut header = vec![(
                headers::CONTENT_TYPE.to_string(),
                self.common_get_content_type().to_string().into(),
            )];

            let auth_type = transformers::MobikwikAuth::try_from(&req.connector_auth_type)?;

            // Add merchant ID header
            if let Some(merchant_id) = &auth_type.merchant_id {
                header.push((headers::MERCHANT_ID.to_string(), merchant_id.clone().into_masked()));
            }

            // Add checksum header if available
            if let Some(checksum) = &auth_type.checksum_secret {
                header.push((headers::CHECKSUM.to_string(), checksum.clone().into_masked()));
            }

            Ok(header)
        }
        fn get_url(
            &self,
            req: &RouterDataV2<Authorize, PaymentFlowData, PaymentsAuthorizeData<T>, PaymentsResponseData>,
        ) -> CustomResult<String, errors::ConnectorError> {
            let is_test = req.resource_common_data.test_mode.unwrap_or(false);
            let base_url = self.connector_base_url_payments(req);
            
            // Use different endpoints based on payment method and test mode
            match req.request.payment_method_type {
                Some(common_enums::PaymentMethodType::UpiIntent) => {
                    if is_test {
                        Ok(format!("{}/debitwallet", base_url))
                    } else {
                        Ok(format!("{}/debitwallet", base_url))
                    }
                }
                _ => Err(errors::ConnectorError::MissingPaymentMethodType.into()),
            }
        }
    }
);

impl<
        T: PaymentMethodDataTypes
            + std::fmt::Debug
            + std::marker::Sync
            + std::marker::Send
            + 'static
            + Serialize,
    > ConnectorCommon for Mobikwik<T>
{
    fn id(&self) -> &'static str {
        "mobikwik"
    }

    fn get_currency_unit(&self) -> CurrencyUnit {
        CurrencyUnit::Base
    }

    fn common_get_content_type(&self) -> &'static str {
        "application/json"
    }

    fn base_url<'a>(&self, connectors: &'a Connectors) -> &'a str {
        connectors.mobikwik.base_url.as_ref()
    }

    fn get_auth_header(
        &self,
        _auth_type: &ConnectorAuthType,
    ) -> CustomResult<Vec<(String, Maskable<String>)>, errors::ConnectorError> {
        // Mobikwik uses custom auth in get_headers
        Ok(vec![])
    }

    fn build_error_response(
        &self,
        res: Response,
        event_builder: Option<&mut ConnectorEvent>,
    ) -> CustomResult<ErrorResponse, errors::ConnectorError> {
        let response: mobikwik::MobikwikErrorResponse = res
            .response
            .parse_struct("MobikwikErrorResponse")
            .change_context(errors::ConnectorError::ResponseDeserializationFailed)?;

        with_error_response_body!(event_builder, response);

        Ok(ErrorResponse {
            status_code: res.status_code,
            code: response.statuscode.to_string(),
            message: response.statusmessage.clone(),
            reason: Some(response.statusmessage),
            attempt_status: None,
            connector_transaction_id: None,
            network_advice_code: None,
            network_decline_code: None,
            network_error_message: None,
        })
    }
}

