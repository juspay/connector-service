impl<
        T: PaymentMethodDataTypes
            + std::fmt::Debug
            + std::marker::Sync
            + std::marker::Send
            + 'static
            + Serialize,
    > ConnectorCommon for Easebuzz<T>
{
    fn id(&self) -> &'static str {
        "easebuzz"
    }

    fn get_currency_unit(&self) -> CurrencyUnit {
        CurrencyUnit::Base
    }

    fn common_get_content_type(&self) -> &'static str {
        "application/json"
    }

    fn base_url<'a>(&self, connectors: &'a Connectors) -> &'a str {
        connectors.easebuzz.base_url.as_ref()
    }

    fn get_auth_header(
        &self,
        _auth_type: &ConnectorAuthType,
    ) -> CustomResult<Vec<(String, Maskable<String>)>, errors::ConnectorError> {
        // Easebuzz uses custom auth in request body
        Ok(vec![])
    }

    fn build_error_response(
        &self,
        res: Response,
        event_builder: Option<&mut ConnectorEvent>,
    ) -> CustomResult<ErrorResponse, errors::ConnectorError> {
        let response: easebuzz::EasebuzzPaymentsResponse = res
            .response
            .parse_struct("EasebuzzPaymentsResponse")
            .change_context(errors::ConnectorError::ResponseDeserializationFailed)?;

        with_error_response_body!(event_builder, response);

        Ok(ErrorResponse {
            status_code: res.status_code,
            code: response.error.as_ref().and_then(|e| e.code.clone()).unwrap_or_default(),
            message: response.error.as_ref().and_then(|e| e.message.clone()),
            reason: response.error.as_ref().and_then(|e| e.reason.clone()),
            attempt_status: None,
            connector_transaction_id: response.txnid,
            network_advice_code: None,
            network_decline_code: None,
            network_error_message: response.error.as_ref().and_then(|e| e.message.clone()),
        })
    }
}

